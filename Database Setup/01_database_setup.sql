CREATE DATABASE ravana
    WITH 
    OWNER = postgres
    ENCODING = 'UTF8'
    LC_COLLATE = 'English_United States.1252'
    LC_CTYPE = 'English_United States.1252'
    TABLESPACE = pg_default
    CONNECTION LIMIT = -1;

GRANT ALL ON DATABASE ravana TO postgres;

GRANT TEMPORARY, CONNECT ON DATABASE ravana TO PUBLIC;